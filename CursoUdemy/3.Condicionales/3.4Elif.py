

num = int(input("Ingrese un numero del 1 al 5: "))

if num == 1:
    print("Tu numero es el uno")
elif num == 2:
    print("Tu numero es el dos")
elif num == 3:
    print("Tu numero es el tres")
elif num == 4:
    print("Tu numero es el cuatro")
elif num == 5:
    print("Tu numero es el cinco")
else:
    print("Has ingresado un numero incorrecto")

